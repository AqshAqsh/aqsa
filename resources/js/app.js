import Echo from 'laravel-echo';
window.Pusher = require('pusher-js');

// Configure Laravel Echo
const echo = new Echo({
    broadcaster: 'pusher',
    key: process.env.MIX_PUSHER_APP_KEY,
    cluster: process.env.MIX_PUSHER_APP_CLUSTER,
    forceTLS: true
});

// Get the authenticated user's ID (you may need to adjust this based on your setup)
const userId = document.querySelector('meta[name="user-id"]').getAttribute('content');

// Listen for the BookingStatusUpdatedEvent
echo.private('user.' + userId)
    .listen('BookingStatusUpdatedEvent', (e) => {
        alert('Your booking status has been updated to: ' + e.booking.status);
        // Optionally, update the UI to reflect the new status
    });

